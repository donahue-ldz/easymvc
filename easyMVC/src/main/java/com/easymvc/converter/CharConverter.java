package com.easymvc.converter;

import com.easymvc.util.StringUtil;

/**
 * CharConverter
 * @author 唐延波
 * @date 2015-5-19
 */
public class CharConverter implements Converter{
	
	@Override
	public Object convert(String param) {
		if(StringUtil.isEmpty(param)){
			return null;
		}
		return param.charAt(0);
	}

}
